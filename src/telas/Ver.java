    public void mostrarContato() {
        ArrayList<Contato> lista = ver.listaContato();
        DefaultTableModel dtm = (DefaultTableModel)tableContato.getModel();
        Object[] row = new Object[3];
        for(int i=0;i<lista.size();i++) {
            row[0]=lista.get(i).getId();
            row[1]=lista.get(i).getNome();
            row[2]=lista.get(i).getTelefone();
            dtm.addRow(row);
        }
    }
    
    public void limparTabela() {
        DefaultTableModel dtm = (DefaultTableModel)tableContato.getModel();
        while(dtm.getRowCount() > 0)
        {
            dtm.removeRow(0);
        }
    }
