    private void btnAtualizeActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAtualizeActionPerformed
        try {
            int id;
            id = (int) tableContato.getValueAt(tableContato.getSelectedRow(), 0);
            Contato atualizar = new Contato(id, txtNome.getText(), txtFone.getText());
            atualizar.atualizar();
            limparTabela();
            mostrarContato();
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Nada escolhido na tabela", "Aviso!", JOptionPane.INFORMATION_MESSAGE);
        }
        
    }//GEN-LAST:event_btnAtualizeActionPerformed

